    internal class miClase
    {
        public int MiClase(int num1,int num2)
        {
            if (num1 > num2)
            {

                return num1 + num2;
            }
            if (num1 < num2)
            {

                return num1 - num2;
            }
            return num1 * num2;
        }
    }
